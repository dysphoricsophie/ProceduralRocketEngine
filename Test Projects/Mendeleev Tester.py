def score_eval(data, prefix_determinant):
    ind = 0
    mp_worth = 0.575
    den_worth = 0.7125
    tc_worth = 0.105
    shc_worth = 0.1075

    name = data[0]
    melt_p = data[1]
    dens = data[2]
    ther_con = data[3]
    spef_hc = data[4]

    try: mp_s = (melt_p*mp_worth)
    except:
        mp_s = 0
        ind = + 1
    try: den_s = (dens*den_worth)
    except:
        den_s = 0
        ind = + 1
    try: tc_s = (ther_con*tc_worth)
    except:
        tc_s = 0
        ind = + 1
    try: shc_s = (spef_hc*shc_worth)
    except:
        shc_s = 0
        ind = + 1

    if ind > 0: name = name + str(f"[SOME DATA UNAVAILABLE:{ind}]")

    total = (mp_s + den_s + tc_s + shc_s)/prefix_determinant
    return total, name
def main():
    scores_d = []
    for i in range(0, len(elemts)-1):
        retrn_data = score_eval(data=elemts[i], prefix_determinant=1)
        scores_d.append(retrn_data)

    scores_d.sort(key=lambda x: x[0], reverse=True)
    file = open("materials_data.log", "w")
    for i in scores_d:
        print(f"Element: {i[1]}")
        print(f"Score: {round(i[0], 3)} U\n")
        file.write(str(f"Element: {i[1]}\n"))
        file.write(str(f"Score: {round(i[0], 3)} U\n\n"))
        sleep(0.1)
    file.close()
if __name__ == "__main__":
    main()
